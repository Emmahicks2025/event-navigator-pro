export const generateSeats = (eventId: string): { section: string; rows: { row: string; seats: number[] }[] }[] => {
  const sections = ['A', 'B', 'C', 'D'];
  const result = sections.map(section => ({
    section,
    rows: Array.from({ length: 8 }, (_, rowIndex) => ({
      row: String.fromCharCode(65 + rowIndex),
      seats: Array.from({ length: 12 }, (_, seatIndex) => seatIndex + 1),
    })),
  }));
  return result;
};
